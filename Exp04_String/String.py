#       String Slicing

mystr = "Sandesh My Text Is Always Good"
print(mystr)
print(mystr[0:2])
print(len(mystr))

print(mystr[::4])
print(mystr[:7])
print(mystr[:])
print(mystr[::-1])

print(mystr.isalnum())
print(mystr.endswith("Good"))
print(mystr.count("s"))
print(mystr.find("Is"))
print(mystr.upper())
print(mystr.replace("Text", "Program"))